    // deixa a string com o tamanho 20 para ajustar no placar
    public String padronizarString(String texto, int tamanho) {
    int tamanhoDesejado = tamanho;
    if (texto.length() < tamanhoDesejado) {
        return String.format("%-" + tamanhoDesejado + "s", texto);
    }
    return texto;
}

