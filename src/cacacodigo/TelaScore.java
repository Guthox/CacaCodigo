    // String com dados para colocar no placar ******************
    
    public String formatarPlacar(String[][] dados){
        String data = "    ";
        outerLoop : for (int i = 0; i < 100; i++){
            for (int j = 0; j < 4; j++){
                if (dados[i][j] == null){
                    break outerLoop;
                }
                switch (j){
                    case 0:
                        data += padronizarString(dados[i][j], 4) + "\t\t";
                        break;
                    case 3:
                        data += padronizarString(dados[i][j], 20) + "\n    ";
                        break;
                    default:
                        data += padronizarString(dados[i][j], 20) + "\t";
                }

            }
        }
        return data;
        
    }
    
