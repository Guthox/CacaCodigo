    private void botaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLoginActionPerformed
       if (botaoLogin.getText().equals("Cadastrar")){
           // se o campo usuario estiver vazio ****************************
           if (campoUser.getText().equals("")){
               JOptionPane.showMessageDialog(null, "Digite o nome de usuário", "Erro", JOptionPane.ERROR_MESSAGE);
           }
           else{
               // Se os campos senhas e usuario estiverem ok
                if (campoSenha.getText().equals("") == false && campoSenha.getText().equals(confirmarSenha.getText())){
                    // Se senha for muito curta
                    if (campoSenha.getText().length() < 5){
                        JOptionPane.showMessageDialog(null, "Senha deve conter pelo menos 5 caracteres", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        // Realiza o cadastro do usuario se ele nao ja estiver cadastrado
                        Banco bd = new Banco(ConnFactory.conectar());
                        if (bd.verificarUsuario(campoUser.getText())){
                            JOptionPane.showMessageDialog(null, "Nome de usuário já está em uso.", "Erro", JOptionPane.ERROR_MESSAGE);
                        }
                        else{
                            // Cadastra o usuario
                            String usuario = campoUser.getText();
                            String senha = campoSenha.getText();
                            boolean tipo = false;
                            if (usuario.length() >= 4){
                                if (usuario.substring(0,4).equals("[CP]")){
                                    tipo = true;
                                }
                            }
                            
                            if (bd.cadastrarUsuario(usuario, senha, tipo)){
                                bd.cadastrarPontuacao(usuario, 0, "99:99");
                                JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso");
                                lbConfirmarSenha.setVisible(false);
                                confirmarSenha.setVisible(false);
                                botaoLogin.setText("Fazer Login");
                                hyperLinkCadastro.setText("Cadastrar!");
                                lbHyperLink.setText("Não possui login?");
                            }
                            else{
                                JOptionPane.showMessageDialog(null, "Erro ao cadastrar usuário.", "Erro", JOptionPane.ERROR_MESSAGE);
                            }
                        }
                        
                    }
                }
                   else{
                   JOptionPane.showMessageDialog(null, "Senha inválida");
                }
           }
       }
       else{
            Banco bd = new Banco(ConnFactory.conectar());
       
            if (bd.logarUsuario(campoUser.getText(), campoSenha.getText())){
                // Inicia o jogo
                ConnFactory.desconectar(bd.getConn());
                dispose();
                TelaEscolha te = new TelaEscolha(campoUser.getText());
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuário ou senha inválidos", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_botaoLoginActionPerformed

    private void hyperLinkCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hyperLinkCadastroMouseClicked
        if (hyperLinkCadastro.getText().equals("Cadastrar!")){
            lbConfirmarSenha.setVisible(true);
            confirmarSenha.setVisible(true);
            botaoLogin.setText("Cadastrar");
            hyperLinkCadastro.setText("Logar!");
            lbHyperLink.setText("Já possui cadastro?");
        }
        else{
            lbConfirmarSenha.setVisible(false);
            confirmarSenha.setVisible(false);
            botaoLogin.setText("Fazer Login");
            hyperLinkCadastro.setText("Cadastrar!");
            lbHyperLink.setText("Não possui login?");
        }
    }//GEN-LAST:event_hyperLinkCadastroMouseClicked

